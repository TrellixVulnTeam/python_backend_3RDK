my_string = "giraffe"

print(my_string)

print("-------")
print("Reverse the string")
print(my_string[::-1])

print("-------")
print("Last Item of the string")
print(my_string[-1])

print("-------")
print("Last Three Item of the string")
print(my_string[-3:])

print("-------")
print("Everything except Last Two Item of the string")
print(my_string[:-2])

print("-------")
print("The first two items, reversed")
print(my_string[1::-1])

print("-------")
print("The last two items, reversed")
print(my_string[:-3:-1])

print("-------")
print("Everything except the last two items, reversed")
print(my_string[-3::-1])
